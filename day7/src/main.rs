fn thruster_signal(program: &[i32], phases: &[i32]) -> Result<i32, ProgramError> {
    let mut input = 0;
    for phase in phases {
        let output = run(&mut program.to_owned(), vec![*phase, input])?;
        input = output[0];
    }

    Ok(input)
}

